# programa para receber 2 números inteiros e realizar todas as operações matematicas

print("aplicativo de operações matematicas")
num1 = float(input("digite o primeiro número inteiro: "))
num2 = float(input("digite o segundo número inteiro: "))
resultado = num1 + num2
print('A soma da soma do número 1 mais o número 2 é: ', resultado)

resultado = num1 - num2
print('A soma da soma do número 1 menos o número 2 é: ', resultado)

resultado = num1 * num2
print('A soma da soma do número 1 vezes o número 2 é: ', resultado)

resultado = num1 / num2
print('A soma da soma do número 1 divisão o número 2 é: ', resultado)