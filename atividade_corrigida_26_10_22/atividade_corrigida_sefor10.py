# maior, menor ou igual a 10

num1 = int(input("digite um número inteiro"))

if (num1 > 10):
    print("O número digitado é maior que o número para adivinhação")

elif (num1 < 10):
    print("O número digitado é menor que o número para adivinhação")

elif (num1 == 10):
    print("Acertou o número!")
